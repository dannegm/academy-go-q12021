// PokemonToCsvRow convert a pokemon structure to a string
func PokemonToCsvRow(p Pokemon) string {
	rowModel := [9]string{
		strconv.Itoa(p.ID),
		p.Name,
		p.TypeOne,
		strconv.Itoa(p.Total),
		strconv.Itoa(p.HealthPoints),
		strconv.Itoa(p.Attack),
		strconv.Itoa(p.Defense),
		strconv.Itoa(p.Speed),
	}
	return strings.Join(rowModel[:], ",") + "\n"
}

// PokemonFromString return a Pokemon from a string
func PokemonFromString(csvRow string) Pokemon {
	pokeRow := strings.Split(csvRow, ",")

	id, _ := strconv.Atoi(pokeRow[0])
	total, _ := strconv.Atoi(pokeRow[4])
	healthPoints, _ := strconv.Atoi(pokeRow[5])
	attack, _ := strconv.Atoi(pokeRow[6])
	defense, _ := strconv.Atoi(pokeRow[7])
	speed, _ := strconv.Atoi(pokeRow[10])

	return Pokemon{
		ID:           id,
		Name:         pokeRow[1],
		TypeOne:      pokeRow[2],
		Total:        total,
		HealthPoints: healthPoints,
		Attack:       attack,
		Defense:      defense,
		Speed:        speed,
	}

}

// StorePokemonInCsv store a pokemin in a file
func StorePokemonInCsv(pokemon Pokemon) error {
	poke := PokemonToCsvRow(pokemon)

	readContent, err := helpers.ReadFile(fmt.Sprintf("%s/pokemon_v2.csv", constants.AssetsPath))

	if err != nil {
		return err
	}

	prevContent := strings.Join(readContent[:], "\n")
	content := prevContent + poke
	return helpers.SaveToFile(fmt.Sprintf("%s/pokemon_v2.csv", constants.AssetsPath), content)
}

// MapPokemonApi transform the response of PokeAPi to a local Pokemon model
func MapPokemonApi(pokeApi *PokemonApi) Pokemon {
	return Pokemon{
		ID:           pokeApi.ID,
		Name:         pokeApi.Name,
		TypeOne:      pokeApi.Types[0].Type.Name,
		Total:        pokeApi.BaseExperience,
		HealthPoints: pokeApi.Stats[0].BaseStat,
		Attack:       pokeApi.Stats[1].BaseStat,
		Defense:      pokeApi.Stats[2].BaseStat,
		Speed:        pokeApi.Stats[5].BaseStat,
	}
}
